// Краткая версия с пояснениями каждого ключевого символа прямо в комментариях.
// Подойдёт для быстрого повторения.

// Объяснение основных символов (кратко):
// let — объявление связывания (имени).
// mut — делает связывание изменяемым.
// = — присваивание / инициализация.
// ; — завершает инструкцию.
// {} — блок, ограничивает область видимости.
// :T — явная аннотация типа (например, : f64).
// println! — макрос для вывода; "{}" — плейсхолдер для значения.

fn main() {
    // let x = 5;
    //  - let: создаём связывание с именем x
    //  - =  : присваиваем значение 5
    //  - ;  : завершаем инструкцию
    let x = 5;
    println!("x = {}", x); // {} заменится значением x

    // let mut y = 10;
    //  - mut: позволяет менять значение в том же связывании
    let mut y = 10;
    println!("y = {}", y);
    y = 20; // присваивание нового значения в том же связывании (т.к. y — mut)
    println!("y после присвоения = {}", y);

    // Shadowing: let x = x + 1;
    //  - повторный let создаёт новое связывание с тем же именем (новый x), это не
    //    то же самое, что изменение через mut.
    let x = x + 1;
    println!("x после shadowing = {}", x);

    // Константа: const NAME: Type = value;
    //  - const: значение не меняется.
    const MAX_POINTS: u32 = 100_000;
    println!("MAX_POINTS = {}", MAX_POINTS);
}
