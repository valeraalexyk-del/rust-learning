fn main() {                          // главная функция программы
    let x = 10;                      // создаём переменную x со значением 10

    if x > 5 {                       // если x больше 5
        println!("x больше 5");      // выполняем этот код
    } else {                         // иначе (если условие выше ложное)
        println!("x НЕ больше 5");   // выполняем этот код
    }
}
