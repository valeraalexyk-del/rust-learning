fn main() {                                      // начало программы
    let number = 3;                               // создаём переменную number

    let result = if number % 2 == 0 {             // проверяем: число чётное?
        "чётное"                                  // если да — возвращаем строку "чётное"
    } else {                                      // иначе
        "нечётное"                                // возвращаем строку "нечётное"
    };                                            // точка с запятой завершает выражение

    println!("Число {} — {}", number, result);    // выводим результат
}
