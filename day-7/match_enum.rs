enum Color {                 // объявляем перечисление Color
    Red,                     // вариант Red
    Green,                   // вариант Green
    Blue,                    // вариант Blue
}

fn main() {                  // начало программы
    let c = Color::Green;    // создаём переменную c типа Color

    match c {                // сопоставляем значение
        Color::Red => println!("Красный"),   // если Red
        Color::Green => println!("Зелёный"), // если Green
        Color::Blue => println!("Синий"),    // если Blue
    }
}

/*
Зачем это нужно:
- enum + match — это мощная комбинация.
- Позволяет описывать разные состояния и обрабатывать их.

Где применяется:
- состояния игры
- типы ошибок
- сетевые протоколы
- обработка событий
*/
