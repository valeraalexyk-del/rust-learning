fn main() {                          // начало программы
    let x = 2;                       // создаём переменную x

    match x {                        // начинаем сопоставление с образцом
        1 => println!("Один"),       // если x == 1
        2 => println!("Два"),        // если x == 2
        3 => println!("Три"),        // если x == 3
        _ => println!("Другое число"), // если ни один вариант не подошёл
    }
}

/*
Зачем это нужно:
- match позволяет проверять разные варианты значения.
- Это удобнее и чище, чем писать много if/else.

Где применяется:
- обработка команд
- выбор действий по номеру
- работа с ошибками
- обработка разных типов данных
*/
